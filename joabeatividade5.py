# Digite uma metragem e veja isso em cm e mm:
print("Descubra a metragem de um némero em centímetremos e milímetros")
numero = float(input("Digite um número: "))
centimetros = numero/100
milimetros = numero/1000
print(f'O número em centímetros é: {centimetros}cm e o número em milímetros é: {milimetros}mm')